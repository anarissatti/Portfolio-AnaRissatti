/**
 * skills toggle
 */

const toggleBtnBox = document.querySelector("[data-toggle-box]");
const toggleBtns = document.querySelectorAll("[data-toggle-btn]");
const skillsBox = document.querySelector("[data-skills-box]");

for (let i = 0; i < toggleBtns.length; i++) {
  toggleBtns[i].addEventListener("click", function () {

    elemToggleFunc(toggleBtnBox);
    for (let i = 0; i < toggleBtns.length; i++) { elemToggleFunc(toggleBtns[i]); }
    elemToggleFunc(skillsBox);

  });
}

document.addEventListener("DOMContentLoaded", function () {
  const row = document.getElementById("projectsRow");
  const clones = row.innerHTML;
  row.innerHTML += clones; // Clona o conteúdo para habilitar a rolagem infinita

  function scrollLeft() {
      row.scrollBy({
          left: -200, // Ajuste este valor conforme necessário
          behavior: 'smooth'
      });
      setTimeout(checkScrollPosition, 500);
  }

  function scrollRight() {
      row.scrollBy({
          left: 200, // Ajuste este valor conforme necessário
          behavior: 'smooth'
      });
      setTimeout(checkScrollPosition, 500);
  }

  function checkScrollPosition() {
      if (row.scrollLeft === 0) {
          row.scrollLeft += row.scrollWidth / 2;
      } else if (row.scrollLeft >= row.scrollWidth / 2) {
          row.scrollLeft -= row.scrollWidth / 2;
      }
  }

  window.scrollLeft = scrollLeft;
  window.scrollRight = scrollRight;

  row.addEventListener('scroll', checkScrollPosition);
});

